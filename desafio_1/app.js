// Inicializa a variável 'contador' com o valor 1
let contador = 1;

// Início do laço 'while' que continuará enquanto 'contador' for menor ou igual a 10
while(contador <= 10){
    // Exibe o valor atual de 'contador' no console do navegador
    console.log(contador);

    // Exibe um alerta na tela com o valor atual de 'contador'
    alert(contador);

    // Incrementa o valor de 'contador' em 1
    contador++;
}; // Fim do laço
